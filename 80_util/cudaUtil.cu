void check_cuda_call( const char * msg = "" ) {
  
  cudaError_t error = cudaGetLastError();
  
  if ( error != cudaSuccess ) {
	  
	printf( " check_cuda_call: failed %s, reason: %s \n", msg, cudaGetErrorString( error ));
	exit(0);
	assert( error == cudaSuccess );
  } 
} // ()

